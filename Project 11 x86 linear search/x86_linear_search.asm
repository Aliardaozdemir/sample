.MODEL SMALL
.DATA
ANARRAY 	DB 5,10,15,20,25
MSGFOUND	DB "ELEMENT IS FOUND IN ARRAY $"
MSGNOTFN	DB "ELEMENT IS NOT FOUND IN ARRAY $"
KEY 	        DB 5

PRINT MACRO MSG
		MOV AH,09H
		LEA DX,MSG
		INT 21H
ENDM

.CODE
START:		MOV AX,@DATA
		MOV DS,AX

		LEA SI,ANARRAY
		MOV CX,05
		MOV AL,KEY
 
SEARCH:		MOV BL,[SI]
		CMP AL,BL
		JZ FOUND
		INC SI
		LOOP SEARCH
		PRINT MSGNOTFN
.EXIT


FOUND:		PRINT MSGFOUND
.EXIT
